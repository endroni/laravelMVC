@section('conteudo')

<h2>Bolo de Laranja</h2>

<h3>Ingredientes</h3>

<ul>
    <li>2 ovos</li>
    <li>1 xícara de leite</li>
    <li>1 massa pronta para bolo sabor Laranja</li>
    <li>2 pacotes de leite condensado</li>
    <li>Chocolate em pó a gosto</li>    
</ul>

<h3>Preparo</h3>

<ul>
    <li>Pré-aqueça o forno a 180°C por 5 min.</li>
    <li>Misture os 2 ovos com o leite e a massa para bolos e coloque em uma forma.</li>
    <li>Leve para o forno e deixe assar por 30 a 45 minutos.</li>
    <li>Misture o leite condensado com o chocolate e leve ao forno por alguns minutos para fazer a cobertura.</li>
    <li>Retire o bolo do forno e coloque a cobertura. Deguste!</li>
</ul>
@endsection
